// output : 7+9+2=18
// aa1152bb2  => 11

class CharacterWrapperClassDemo {
    public static void main(String[] args) {
        // char -> Character
        char ch1 = 'A';
        char ch2 = '8';

        System.out.println(Character.isLowerCase(ch1));
        System.out.println(Character.isDigit(ch2));
    }
}